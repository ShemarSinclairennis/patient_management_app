    {
     
        $report = new Report;
        $report->patient_id = $request->patient_id;
        $report->service = $request->service;
        $report->department = $request->department;
        $report->description = $request->description;
      
     
        $report->save();
        return back()->withSuccess("Patient added to the system");
    }

